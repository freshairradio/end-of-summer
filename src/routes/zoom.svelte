    <h1 class="text-center">Guide to Zoom</h1>
    <h2>Renaming</h2>
    <ul>
      <li>Open the “Participants” menu</li>
      <li>Find your own name in the list that appears</li>
      <li>Hover over your name, which will show the “More” menu</li>
      <li>In the “More” menu, there’s an option to “Rename”</li>
    </ul>
    <h2>Reactions</h2>
    <ul>
      <li>Click the “Reactions” menu item</li>
      <li>
        Two emojis should pop up on screen, press the one you’d like to use
      </li>
    </ul>
    <h2>Private messaging</h2>
    <ul>
      <li>
        If you need to message your leaders during a zoom call, open the “Chat”
        menu. Depending on your screen size, this might be hidden behind a
        “More” button that you’ll have to click first.
      </li>
      <li>
        Once the chat window is open, you should see a “To: Everyone” at the
        bottom of the screen.
      </li>
      <li>
        Click “Everyone”, which will let you choose a specific person to send a
        message to.
      </li>
    </ul>
    <h2>Sound and Video</h2>
    <ul>
      <li>
        When you first join a meeting, there will be a popup with a blue button
        to join with Internet Audio - click this. There are other options on
        that popup, but you can ignore those.
      </li>
      <li>
        In the bottom right of the screen there are two icons - a camera and a
        microphone.
      </li>
      <li>
        To mute/turn off your camera, press the button, which should put a red
        diagonal line through it.
      </li>
      <li>To unmute/turn your camera back on, press the button again.</li>
    </ul>
